// 🎯 核心思想：动态伸缩的滑动窗口

// 暴力解法是用两个 `for` 循环寻找所有可能的子数组，时间复杂度是极其糟糕的 $O(n^2)$。
// 滑动窗口的核心思想是：**像一个拥有弹性的毛毛虫，在数组上爬行。** 我们只用一个 `for` 循环，就能完成原先需要两个循环才能做完的事。这里的核心痛点在于：这个唯一的循环指针，到底该指向窗口的**起始位置**，还是**结束位置**？

