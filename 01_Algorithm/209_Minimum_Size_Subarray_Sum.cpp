// 💡 面试核心考点：
// 直击灵魂的拷问一：为什么 for 循环里的指针 right 代表的是窗口的“终点”？能写成“起点”吗？
//     绝对不能！ 如果 for 循环的指针代表起点，那么你为了找到满足条件的终点，就不得不再嵌套一个内部循环去向后遍历。这样代码瞬间就退化成了 $O(n^2)$ 的暴力解法，滑动窗口的意义就完全丧失了。
// 所以，外层循环必须是终点指针在前面“探路”。
// 直击灵魂的拷问二：缩小窗口时，为什么必须用 while 而不是 if？
//     这是新手极其容易踩坑的地方！假设目标 target = 7，当前数组是 [1, 1, 1, 1, 100]。当右指针走到 100 时，窗口内的和突然变成了 104。满足 sum >= 7，触发条件。如果我们只用 if，左指针 left 只会向右移动一次，吐出一个 1，此时和为 103，依然大于 7！但代码却跳出了判断，错失了继续缩短窗口的机会。用 while 才能保证左边界持续向右挤压，直到把多余的元素全部吐干净。
// 直击灵魂的拷问三：代码里 for 循环嵌套了一个 while 循环，为什么时间复杂度依然是 $O(n)$ 而不是 $O(n^2)$？
//     不要被嵌套的循环代码结构骗了。分析时间复杂度要看每个元素被操作的次数。在这个算法中，数组里的每一个元素，最多被右指针 right 吃进去一次（进窗口），最多被左指针 left 吐出来一次（出窗口）。
// 每个元素的操作次数是常量级别的（最多进出各一次），总操作次数约为 $2n$。因此，时间复杂度依然是极其优秀的 $O(n)$。